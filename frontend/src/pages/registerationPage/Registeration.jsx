import { useState } from "react";
import "../../styles/Registeration.css";
import SignInForm from "./SignIn";
import SignUpForm from "./SignUp";
import logo from "../../assets/OrthoLegal-Logo-COL2-02 (2).jpg";

export default function Registeration() {
  const [type, setType] = useState("signIn");
  const handleOnClick = (text) => {
    if (text !== type) {
      setType(text);
      return;
    }
  };
  const containerClass =
    "container " + (type === "signUp" ? "right-panel-active" : "");
  return (
    <div className="Registeration">
      <div className="titleLogo d-flex justify-content-center align-items-center m-5 gap-4">
        <h1>OrthoLegalApp</h1>
        <img className="logoDesign" src={logo} alt="logo" />
      </div>
      <div className={containerClass} id="container">
        <SignUpForm />
        <SignInForm />
        <div className="overlay-container">
          <div className="overlay">
            <div className="overlay-panel overlay-left">
              <h1>Welcome Back!</h1>
              <p>
                To keep connected with us please login with your personal info
              </p>
              <button
                className="ghost"
                id="signIn"
                onClick={() => handleOnClick("signIn")}
              >
                Sign In
              </button>
            </div>
            <div className="overlay-panel overlay-right">
              <h1>Hello!</h1>
              <p>Enter your personal details and start journey with us</p>
              <button
                className="ghost "
                id="signUp"
                onClick={() => handleOnClick("signUp")}
              >
                Sign Up
              </button>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}
